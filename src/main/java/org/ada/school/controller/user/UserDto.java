package org.ada.school.controller.user;

public class UserDto
{
    String name;

    String lastName;

    String email;

    public UserDto()
    {
    }

    public String getName()
    {
        return name;
    }

    public String getLastName()
    {
        return lastName;
    }

    public String getEmail()
    {
        return email;
    }
}
